class Product {
  final int id;
  final String title;
  final double price;
  final String image;
  final String category;
  final double rating;
  final String description;

  const Product({
    required this.id,
    required this.title,
    required this.price,
    required this.image,
    required this.category,
    required this.rating,
    required this.description
  });
}
